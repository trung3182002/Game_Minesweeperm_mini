    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int selectedRow = table_diem.getSelectedRow();
        int column = 0;
        String value = table_diem.getModel().getValueAt(selectedRow, column).toString();
        int row = table_diem.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn thành tích cần xoá");
        } else {
            int n = JOptionPane.showConfirmDialog(this, "Thông báo!", "Bạn có muốn Xóa thông tin không?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (n == JOptionPane.NO_OPTION) {
                return;
            } else {
                try {
                    String sqlDelete = "delete from DIEM where Player ='" + value + "'";
                    int x = statement.executeUpdate(sqlDelete);
                    if (x > 0) {
                        JOptionPane.showMessageDialog(this, "Xóa thành tích thành công");
                        String sqlSelect = "select * from DIEM order by KETQUA DESC";
                        hienThi(sqlSelect);
                    } else {
                        System.out.println("Xóa thành tích thất bại");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(BXH_admin.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

