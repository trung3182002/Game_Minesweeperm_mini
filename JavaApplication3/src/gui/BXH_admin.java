    private void hienThi(String sql) {
        try {
            try {
                statement = connection.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(BXH_admin.class.getName()).log(Level.SEVERE, null, ex);
            }
            dtm.setRowCount(0);
            resultSet = statement.executeQuery(sql);
            while (resultSet.next()) {
                String user = resultSet.getString("Player");
                int point = resultSet.getInt("KETQUA");
                int time = resultSet.getInt("THOIGIAN");
                Vector<Object> v = new Vector<>();
                v.add(user);
                v.add(point);
                v.add(time);
                dtm.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(BXH_admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

