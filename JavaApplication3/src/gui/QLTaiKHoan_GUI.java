    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:

        int row = tb_taiKhoan.getSelectedRow();
        if(row==-1)
        {
            JOptionPane.showMessageDialog(this, "Chon dong can xoa");
            return;
        }
        else
        {
            int n = JOptionPane.showConfirmDialog(this, "Ban co chac chan muon xoa ?","Thong bao",JOptionPane.YES_NO_OPTION);
            if(n==JOptionPane.NO_OPTION)
            {
                JOptionPane.showMessageDialog(this, "Xoa that bai");
                return;
            }
            else{
                String user = (String) tb_taiKhoan.getValueAt(row, 0);
                QL_TaiKhoanDAO.xoaTaiKhoan(user);
                JOptionPane.showMessageDialog(this, "Xoa du lieu thanh cong");
                hienThi();
            }
        }

    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
        // TODO add your handling code here:

           String user = txt_username.getText();
            String pass = txt_pass.getText();
        //thêm
        if(them)
        {

            int selectedIndex = cbo_quyen.getSelectedIndex();

            //int quyen = Integer.valueOf((int)cbo_quyen.getSelectedItem());

            TAIKHOAN tk =new TAIKHOAN();
            tk.setUserName(user);
             tk.setPass(pass);
            tk.setQuyen(selectedIndex);
            QL_TaiKhoanDAO.themTaiKhoan(tk);
            JOptionPane.showMessageDialog(this, "Them Tai Khoan Thanh Cong");

            hienThi();
            them=false;
        
        }
        else
        {
            
             txt_username.enable();
              txt_pass.enable();
         
            if(!user.equals("") || !pass.equals(""))
            {

                          TAIKHOAN tk = new TAIKHOAN(user,pass,0,"Off");
                          QL_TaiKhoanDAO.capNhatTaiKhoan(tk);
                              hienThi();
                          JOptionPane.showMessageDialog(this, "Cập nhật thành thông","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            }
             else
            {
                          JOptionPane.showMessageDialog(this, "user và passwword không thể trống ","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            }
        
        }


    }//GEN-LAST:event_btn_luuActionPerformed

