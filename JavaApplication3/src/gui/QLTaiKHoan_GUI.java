     private void hienThi() {
        
        ArrayList<TAIKHOAN> dsTK = QL_TaiKhoanDAO.layDanhSachTaiKhoan();
       
       
        dtm.setRowCount(0);
        for(TAIKHOAN tk : dsTK)
        {
            Vector<Object> vec = new Vector<Object>();
            vec.add(tk.getUserName());
            vec.add(tk.getPass());
            vec.add(tk.getQuyen());
             vec.add(tk.getTrangThai());
            dtm.addRow(vec);
        }
        
        tb_taiKhoan.setModel(dtm);
       }

    
